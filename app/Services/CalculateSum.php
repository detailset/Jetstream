<?php
namespace App\Services;

class CalculateSum {

      public function sumNumbers(){

            $num1 = 24;
            $num2 = 24;

            return  $num1 + $num2;

      }


      public function substractNumbers(){

            $num1 = 24;
            $num2 = 14;

            return  $num1 - $num2;

      }


      public function multiplyNumbers(){

            $num1 = 4;
            $num2 = 4;

            return  $num1 * $num2;

      }


      
}